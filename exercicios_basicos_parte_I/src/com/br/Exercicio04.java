package com.br;
// Escreva um programa Java para imprimir o resultado das seguintes operações.

// a. -5 + 8 * 6
// b. (55+9) % 9
// c. 20 + -3*5 / 8
// d. 5 + 15 / 3 * 2 - 8% 3

public class Exercicio04 {

	public static void main(String[] args) {
		int respostaA = -5 + 8 * 6;
		int respostaB = (55 + 9) % 9;
		int respostaC = 20 + -3 * 5 / 8;
		int respostaD = 5 + 15 / 3 * 2 - 8 % 3;

		System.out.println(respostaA);
		System.out.println(respostaB);
		System.out.println(respostaC);
		System.out.println(respostaD);
	}
}